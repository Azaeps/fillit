#include "fillit.h"

int		check_s(char *s)
{
	int x;
	int y;

	x = 0;
	y = 0;
	while (s[x])
	{
		if (s[x] == '#')
		{
			if (s[x + 1] == '#' || s[x - 1] == '#' || s[x + 5] == '#'
					|| s[x - 5] == '#')
				y++;
		}
		x++;
	}
	if (y == 4)
		return (1);
	return (0);
}
